
public class _10_for_loop
{

	public static void main(String[] args)
	{
		//the aim is to learn for loop
		//assume an integer i with a value of zero and increment untill it becomes 25 and print it in the output
		int i;
		
		for(i=0;i<=25;i++);
		{
		System.out.printf("The value of i is:%d \n",i);	
		}		

	}

}
